import torch

def ask_vlm(input_image):
    prompt = """
Look carefully at the image.

How many separate red highlighted regions are visible?

Answer using exactly one of these:
0, 1, 2, 3, 4+
"""

    messages = [
        {
            "role": "user",
            "content": [
                {
                    "type": "image",
                    "image": input_image
                },
                {
                    "type": "text",
                    "text": prompt
                }
            ]
        }
    ]

    inputs = processor.apply_chat_template(
        messages,
        tokenize=True,
        add_generation_prompt=True,
        return_dict=True,
        return_tensors="pt"
    )

    inputs = inputs.to(model.device)

    with torch.no_grad():
        generated_ids = model.generate(
            **inputs,
            max_new_tokens=20,
            do_sample=False
        )

    output_ids = generated_ids[:, inputs["input_ids"].shape[1]:]

    answer = processor.batch_decode(
        output_ids,
        skip_special_tokens=True
    )[0]

    return answer.strip()

