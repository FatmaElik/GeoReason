def normalize_answer(answer):
    """
    Model cevabını şu sınıflardan birine dönüştürür:
    0, 1, 2, 3, 4+
    """
    text = str(answer).strip().lower()

    if "4+" in text or "4 +" in text:
        return "4+"

    numbers = re.findall(r"\d+", text)

    if not numbers:
        return "unknown"

    number = int(numbers[0])

    if number >= 4:
        return "4+"

    return str(number)

def prepare_sample(row, base_folder):
    image_path = os.path.join(base_folder, row["image_path"])
    label_path = os.path.join(base_folder, row["label_path"])

    image = Image.open(image_path).convert("RGB")
    image_array = np.array(image)

    label_array = np.array(Image.open(label_path))

    if label_array.ndim == 3:
        label_array = label_array[:, :, 0]

    mask = label_array > 0

    _, region_count = count_components(mask)

    expected_answer = "4+" if region_count >= 4 else str(region_count)

    overlay = image_array.copy()

    overlay[mask] = (
        0.5 * overlay[mask]
        + 0.5 * np.array([255, 0, 0])
    ).astype(np.uint8)

    overlay_image = Image.fromarray(overlay)

    rotated_image = overlay_image.rotate(
        -90,
        expand=True
    )

    return overlay_image, rotated_image, region_count, expected_answer

