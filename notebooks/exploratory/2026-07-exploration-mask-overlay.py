def get_direction_from_mask(label_path):
    mask = np.array(Image.open(label_path))

    if mask.ndim == 3:
        mask = mask[:, :, 0]

    mask = mask > 0

    # Maskede hedef yoksa
    if not mask.any():
        return "unclear"

    y_coords, x_coords = np.where(mask)

    center_x = x_coords.mean() / mask.shape[1]
    center_y = y_coords.mean() / mask.shape[0]

    # Ortadaki alanı center kabul et
    if 0.33 <= center_x <= 0.67 and 0.33 <= center_y <= 0.67:
        return "center"

    horizontal_distance = abs(center_x - 0.5)
    vertical_distance = abs(center_y - 0.5)

    # Baskın ekseni seç
    if horizontal_distance > vertical_distance:
        return "west" if center_x < 0.5 else "east"
    else:
        return "north" if center_y < 0.5 else "south"

